// What is Variable? 
// A variable is a named storage location in a computer's memory that can hold a value.
// It allows you to store and manipulate data in your programs. 
// In C++, you can declare a variable by specifying its type and name.
// For example:
#include <iostream>
using namespace std;
int main() {
    int age; // Declaring a variable named 'age' of type int
    age = 25; // Assigning a value to the variable 'age'
    
    cout << "My age is: " << age << endl; // Outputting the value of 'age' to the console
    
    return 0;
}
// In this example, we declared a variable named 'age' of type int, assigned it the value 25, and then printed it to the console.
// Variables can hold different types of data, such as integers, floating-point numbers, characters, and more.
// You can also declare and initialize a variable in a single line, like this:
    int age = 25; // Declaring and initializing the variable 'age' in one line
